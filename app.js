function setLang(lang){
    // Change body direction and font for Arabic
    document.body.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
    document.documentElement.setAttribute("lang", lang);

    // Update all translated elements
    document.querySelectorAll("[data-i]").forEach(el=>{
        const key = el.getAttribute("data-i");
        el.textContent = T[lang][key] || T.en[key]; // Fallback to English
    });

    // Update active chip
    document.querySelectorAll(".lang .chip").forEach(chip => {
        chip.classList.remove("active");
        if (chip.getAttribute("data-lang") === lang) {
            chip.classList.add("active");
        }
    });
}

// Event listeners for language chips
document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll(".lang .chip").forEach(chip => {
        chip.addEventListener("click", () => {
            const lang = chip.getAttribute("data-lang");
            setLang(lang);
        });
    });

    // Initialize with default (or saved) language, using 'en' as default
    setLang(document.querySelector(".lang .chip.active")?.getAttribute("data-lang") || 'en');
});